            if (imaPraznihPolja())
                throw new Exception("Nisu sva polja popunjena!");          
            if (!txtNovaLozinka1.getText().equals(txtNovaLozinka2.getText())) 
                throw new Exception("Nove lozinke se ne poklapaju!");
